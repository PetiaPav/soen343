
class User:
    def __init__(self, user_id, first_name, last_name, address, email, phone, admin, password):
        self.id = user_id
        self.first_name = first_name
        self.last_name = last_name
        self.address = address
        self.email = email
        self.phone = phone
        self.admin = admin
        self.password = password
